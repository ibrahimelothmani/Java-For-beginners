public class Main {
    public static void main(String[] args) {

        String str = "hello world";
        char target = 'l';
        int count = 0;
        for (int i = 0; i < str.length(); i++) {
            if (str.charAt(i) == target) {
                count++;
            }
        }
        System.out.println("Occurrences of 'l': " + count);

    }
}